public class RetrieveEntriesException extends Exception {
	private static final long serialVersionUID = 1L;

	public RetrieveEntriesException() {
		super();
	}

	public RetrieveEntriesException(String message) {
		super(message);
	}

	public RetrieveEntriesException(String message, Throwable cause) {
		super(message, cause);
	}

	public RetrieveEntriesException(Throwable cause) {
		super(cause);
	}
}

